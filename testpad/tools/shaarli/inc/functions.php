  function getAndShowFilters($content, $filters, $title, $nb){

    foreach ($content->channel->item as $entry) {
      foreach ($entry->category as $category) {
        $category;
        if (substr($category, 0, 1) == $nb) {
          array_push($filters, substr($category, 1));
        }
      }
    }
    $filters = array_unique($filters);
    sort($filters);

    echo '<ul class="'.$title.'">';
    echo '<li><h1>'.$title.':</h1></li>';
    foreach ($filters as $filter) {
      if (strlen($filter) > 1) {
        echo '<li class="'.$filter.'">'.str_replace('_', ' ' , $filter).'</li>';
      }
    }
    echo '</ul>';

  }

